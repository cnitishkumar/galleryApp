class Gallery extends Component {
  state = {currentImageID: 0}

  onClickThumbnail = id => {
    this.setState({currentImageID: id})
  }

  render() {
    const {currentImageID} = this.state

    const filteredList = imagesList.filter(
      eachImageDetails => eachImageDetails.id === currentImageID,
    )
    const {imageUrl, imageAltText} = filteredList[0]
    console.log(filteredList[0])
    return (
      <div className="bg-container">
        <div className="image-container">
          <img src={imageUrl} className="gallery-image" alt={imageAltText} />
        </div>
        <h1 className="heading">Nature Photography</h1>
        <p className="tag-line">Nature photography by Rahul</p>
        <div>
          <ul className="ul-el">
            {imagesList.map(each => (
              <ThumbnailItem
                thumbnailDetails={each}
                key={each.id}
                onClickThumbnail={this.onClickThumbnail}
                isActiveThumbnail={each.id === currentImageID}
              />
            ))}
          </ul>
        </div>
      </div>
    )
  }
}

export default Gallery
